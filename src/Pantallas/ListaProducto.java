    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       
        String textoBusqueda = txtBuscar.getText().toLowerCase();

        if (ListaProductos.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                    "No hay cursos");
            return;
        }

        DefaultListModel<String> modeloLista 
                = (DefaultListModel<String>) lstProductos.getModel();

        OptionalInt index = IntStream.range(0,
                modeloLista.size())
                .filter(i -> modeloLista.getElementAt(i)
                .toLowerCase().contains(textoBusqueda))
                .findFirst();

        if (index.isPresent()) {
            lstProductos.setSelectedIndex(index.getAsInt());
        } else {
            JOptionPane.showMessageDialog(
                    this, 
                    "El producto no se encuentra en lal lista", 
                    "BÃºsqueda producto", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

